#!/usr/bin/env node
// Génère un fichier cookies Netscape pour yt-dlp depuis un sessionid Instagram
// Usage: node scripts/generate-instagram-cookies.js <sessionid> [output_path]

const sessionId = process.argv[2];
const outputPath = process.argv[3] || 'instagram-cookies.txt';

if (!sessionId) {
  console.error('Usage: node scripts/generate-instagram-cookies.js <sessionid> [output_path]');
  process.exit(1);
}

const { writeFileSync } = require('fs');

// Format Netscape cookies (requis par yt-dlp)
// domain  include_subdomains  path  secure  expiry  name  value
const expires = Math.floor(Date.now() / 1000) + 365 * 24 * 3600; // 1 an

const lines = [
  '# Netscape HTTP Cookie File',
  '# Generated by Commis for yt-dlp',
  '',
  `.instagram.com\tTRUE\t/\tTRUE\t${expires}\tsessionid\t${sessionId}`,
  `.instagram.com\tTRUE\t/\tTRUE\t${expires}\tds_user_id\t${sessionId.split('%3A')[0]}`,
].join('\n');

writeFileSync(outputPath, lines, 'utf8');
console.log(`✓ Cookies écrits dans ${outputPath}`);
